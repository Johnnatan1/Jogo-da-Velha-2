public void VerificaJogadorDaVez(){
    if(jog1&&jog2){
        this.op=1;
        jog1=false;
        jog2=true;
        txtvisor.setText("vez do jogador O");
    }else if(jog1&&jog2==false){
        this.op=1;
        jog1=false;
        jog2=true;
        txtvisor.setText("vez do jogador O"); 
        
    }else{
        this.op=2;
        jog1=true;
        jog2=false;
        txtvisor.setText("vez do jogador X");
    }
}
