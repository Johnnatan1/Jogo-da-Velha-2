public void VerificaPosicoes(){
    contpos=0;
    for(i=0;i<3;i++){
        for(k=0;k<3;k++){
            if(matriz[i][k]=="x"|matriz[i][k]=="o"){
                contpos+=1;
                
            }
        }
    }
}
public void VerificaVencedor(){
    //linhas
    lin1=matriz[0][0]+matriz[0][1]+matriz[0][2];
    lin2=matriz[1][0]+matriz[1][1]+matriz[1][2];
    lin3=matriz[2][0]+matriz[2][1]+matriz[2][2];
    //colunas
    colun1=matriz[0][0]+matriz[1][0]+matriz[2][0];
    colun2=matriz[0][1]+matriz[1][1]+matriz[2][1];
    colun3=matriz[0][2]+matriz[1][2]+matriz[2][2];
    //diagonais
    diago1=matriz[0][0]+matriz[1][1]+matriz[2][2];
    diago2=matriz[0][2]+matriz[1][1]+matriz[2][0];
    
    if(lin1.equals("xxx")|lin2.equals("xxx")|lin3.equals("xxx")|colun1.equals("xxx")|colun2.equals("xxx")|colun3.equals("xxx")|diago1.equals("xxx")|diago2.equals("xxx")){
        txtvisor.setText("o vencedor é o X");
        DesativaBotoes();
        
    }else if(lin1.equals("ooo")|lin2.equals("ooo")|lin3.equals("ooo")|colun1.equals("ooo")|colun2.equals("ooo")|colun3.equals("ooo")|diago1.equals("ooo")|diago2.equals("ooo")){
        txtvisor.setText("o vencedor é o O");
        DesativaBotoes();
    }else if(contpos==9){
        txtvisor.setText("Deu Velha");
    }
}
